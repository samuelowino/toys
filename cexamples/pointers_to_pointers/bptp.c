#include <stdio.h>
int main(){
	int x = 100;
	int *p = &x;
	int **pp = &p;
	/*	    +---------+         +---------+         +---------+
 	* x  -----> |   50    |         |         |         |         |
 	*           +---------+         +---------+         +---------+
 	* Addr:     0x1000              0x1008              0x1010
 	*           ^                   ^                   ^
 	*           |                   |                   |
 	*           |                   |                   |
 	*           |                +--+                   |
	*           |                |                      |
 	* p  ------>| 0x1000 --------+                      |
 	*           +---------+                             |
 	*           |  &x     |                             |
 	*           +---------+                             |
 	* Addr:     0x1008                                  |
 	*           ^                                       |
 	*           |                                       |
 	* pp ------>| 0x1008 -------------------------------+
 	*           +---------+
 	*           |  &p     |
 	*           +---------+
 	* Addr:     0x1010
 	*
 	*/
	printf("\nvalue in x[%d]\t\taddr[%p]",x,(void*)&x);
	printf("\nvalue in *p[%d]\taddr[%p]",*p,(void*)&p);
	printf("\nvalue in **pp[%d]\taddr[%p]",**pp,(void*)&pp);
	return 0;
}

